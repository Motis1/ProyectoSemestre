    private void mostrarCanchas() {
        this.cmbCanchas.removeAllItems();
        this.cmbCanchas.addItem("Seleccione aqui...");
        for (int i = 0; i < this.myPrincipal.getCanchas().size(); i++) {
            this.cmbCanchas.addItem(this.myPrincipal.getCanchas().get(i));
        }
    }

    private void mostrarClientes(ArrayList<String> clientes) {
        this.cmbClientes.removeAllItems();
        this.cmbClientes.addItem("Seleccione aqui...");
        for (int i = 0; i < this.myPrincipal.getClientes().size(); i++) {
            this.cmbClientes.addItem(this.myPrincipal.getClientes().get(i));
        }
    }
