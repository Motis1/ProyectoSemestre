    private void cmdRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = this.txtNombre.getText();
        String cedula = this.txtCedula.getText();
        String telefono = this.txtTelefono.getText();
        
        String registroCliente = this.myPrincipal.registrarCliente(cedula, nombre, telefono);
        this.txtMostrar.setText(registroCliente);
        
        if(registroCliente.contains("CLIENTE REGISTRADO CON EXITO")){
            this.mostrarClientes(this.myPrincipal.getClientes());
            this.limpiarCliente();
        }
    }//GEN-LAST:event_cmdRegistrarActionPerformed

    private void cmdReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdReservarActionPerformed
        // TODO add your handling code here:
        this.txtMostrar.setText("");
        java.util.Date fechaInicio = (Date) jsFechaInicio.getValue();
        String cancha = this.cmbCanchas.getSelectedItem().toString();
        String cliente = this.cmbClientes.getSelectedItem().toString();
        int tiempo = Integer.parseInt(this.cmdHoras.getSelectedItem().toString());
        int cantidad = Integer.parseInt(this.cmdHoras.getSelectedItem().toString());
        
        String reporte = this.myPrincipal.reservar(cancha, cliente,cantidad, fechaInicio);
        this.txtMostrar.setText(reporte);    
    }//GEN-LAST:event_cmdReservarActionPerformed

